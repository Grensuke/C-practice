#include <iostream>

using namespace std;

int main() {

    int num;
    cout << "Enter a number:";
    cin >> num;
    int x;
    x > 0;
    x != num;
    x != 1;
    


    if ( (num % x ) != 0 ) {
        cout << "The given number is a prime number.";
    }

    else {
        cout << "The given number is a composite number.";
    }
}